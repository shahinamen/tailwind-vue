export function generateLowerCase(input) {
  return input.toLowerCase()
}
export function generateUpperCase(input) {
  return input.toUpperCase()
}

export function generateFruitList() {
  const fruits = [
    'apple',
    'banana',
    'cherry',
    'date',
    'elderberry',
    'fig',
    'grape',
    'honeydew',
    'kiwi',
    'lemon',
  ]
  return fruits
}
export function calculationPercentage(value, total) {
  return (value / total) * 100
}
